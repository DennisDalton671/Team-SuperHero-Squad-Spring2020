package Model;

public class Inventory {

	/* Simple class to make an inventory object for the table view */
	
	private String items;

	public Inventory(String items) {
		this.items = items;
	}
	
	public String getItems() {
		return items;
	}

	public void setItems(String items) {
		this.items = items;
	}
	
	
	
}
